package jdbc_user_eb13;

public class Users {
	private int id;
	private String name;
	private String email;
	private String password;
	private String address;
	private String facebook_password;
	private String instagram__password;
	private String whatsapp_password;
	private String snapchat_password;
	private String twitter_password;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getFacebook_password() {
		return facebook_password;
	}
	public void setFacebook_password(String facebook_password) {
		this.facebook_password = facebook_password;
	}
	public String getInstagram__password() {
		return instagram__password;
	}
	public void setInstagram__password(String instagram__password) {
		this.instagram__password = instagram__password;
	}
	public String getWhatsapp_password() {
		return whatsapp_password;
	}
	public void setWhatsapp_password(String whatsapp_password) {
		this.whatsapp_password = whatsapp_password;
	}
	public String getSnapchat_password() {
		return snapchat_password;
	}
	public void setSnapchat_password(String snapchat_password) {
		this.snapchat_password = snapchat_password;
	}
	public String getTwitter_password() {
		return twitter_password;
	}
	public void setTwitter_password(String twitter_password) {
		this.twitter_password = twitter_password;
	}
	
	
	
	

}
